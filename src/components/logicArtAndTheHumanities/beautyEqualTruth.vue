            <h6 class="left-align">
                March 26, 2002
            </h6>
            <h4 class="left-align">
                The Most Seductive Equation in Science: Beauty Equals Truth
            </h4>
            <h6 class="left-align">
                By DENNIS OVERBYE
            </h6>
            <p class="left-align">
                In the fall of 1915, Albert Einstein, living amid bachelor 
                clutter on coffee, tobacco and loneliness in Berlin, was close to 
                scrawling the final touches to a new theory of gravity that he had 
                pursued through mathematical and logical labyrinths for nearly a 
                decade. But first he had to see what his theory had to say about 
                the planet Mercury, whose puzzling orbit around the Sun defied the 
                Newtonian correctness that had long ruled the cosmos and science. 
                The result was a kind of cosmic "boing" that changed his life.
            </p>
            <p class="left-align">
                Einstein's general theory of relativity, as it was known, 
                described gravity as warped space-time. It had no fudge factors — 
                no dials to twiddle. When the calculation nailed Mercury's orbit 
                Einstein had heart palpitations. Something inside him snapped, he 
                later reported, and whatever doubt he had harbored about his 
                theory was transformed into what a friend called "savage 
                certainty." He later told a student that it would have been 
                "too bad for God," if the theory had been subsequently disproved.  
            </p>
            <p class="left-align">
                The experience went a long way toward convincing Einstein that 
                mathematics could be a telegraph line to God, and he spent most 
                of the rest of his life in an increasingly abstract and 
                ultimately fruitless pursuit of a unified theory of physics.
            </p>
            <p class="left-align">
                Rare indeed is the scientist who has not at one point or other 
                been seduced by the beauty of his own equations and dumbfounded 
                by what the physicist Dr. Eugene Wigner of Princeton once called 
                the "unreasonable effectiveness of mathematics" in describing the 
                world.
            </p>
            <p class="left-align">
                The endless fall of the moon, the fairy glow of a rainbow, the 
                crush of a nuclear shock wave are all explicable by scratches on 
                a piece of paper, that is to say, equations. Every time an 
                airplane safely touches down on time, a computer boots up, or a 
                cake comes out right, the miracle is recreated. "The most 
                incomprehensible thing about the universe is that it is 
                comprehensible," Einstein said.
            </p>
            <p class="left-align">
                Math is the language of physics, but is it the language of God?
            </p>
            <p class="left-align">
                Mathematicians often say that they feel as if their theorems and 
                laws have an objective reality, like Plato's perfect realm of 
                ideas, which they do not create or construct as much as simply 
                discover. But the equating of math with reality, others say, 
                consigns vast arenas of experience to the darkness. There are no 
                mathematical explanations yet for life, love or consciousness.
            </p>
            <p class="left-align">
                "As far as the laws of mathematics refer to reality, they are not 
                certain; and as far as they are certain, they do not refer to 
                reality," said Einstein. 
            </p>
            <p class="left-align">
                He maintained that it should be possible to explain scientific 
                principles in words to a child, but his followers often argue that 
                words alone cannot convey the glories of physics, that there is a 
                beauty apparent only to the mathematically adept.
            </p>
            <p class="left-align">
                That inhuman beauty has long been a lodestone for physicists, says 
                Dr. Graham Farmelo, a physicist at the Science Museum in London 
                and an editor of "It Must Be Beautiful: Great Equations of Modern 
                Science."
            </p>
            <p class="left-align">
                "You can write it on the palm of your hand and it shapes the 
                universe," Dr. Farmelo said of Einstein's gravitational equation, 
                the one that produced heart palpitations. He compared the feeling 
                of understanding such an equation to the emotions you experience 
                "when you take possession of a great painting or a poem."
            </p>
            <p class="left-align">
                In the hopes of getting the rest of us to take possession some of 
                our intellectual heritage, Dr. Farmelo recruited scientists, 
                historians and science writers to write about the life and times 
                of 11 of the most powerful or notorious equations of 20th century 
                science. 
            </p>
            <p class="left-align">
                The book is partly a meditation on mathematical beauty, possibly 
                a difficult concept for many Americans right now as they confront 
                their tax forms. But as Dr. Farmelo noted in an interview, even 
                the most recalcitrant of us have had glimpses of mathematical 
                grace when, say, our checkbooks balanced.
            </p>
            <p class="left-align">
                Imagine that your withholdings always turned out to be exactly 
                equal to the tax you wind up owing. Or that your car's odometer 
                turned over to all zeros every year on your birthday no matter 
                how far you thought you had driven. Such occurrences would be 
                evidence of patterns in your financial affairs or driving habits 
                that might be helpful in preparing tax returns or scheduling car 
                maintenance.
            </p>
            <p class="left-align">
                The pattern most highly prized in recent modern physics has been 
                symmetry. Just as faces and snowflakes are prettier for their 
                symmetrical patterns, so physical laws are considered more 
                beautiful if they keep the same form when we change things by, 
                for example, moving to the other side of the universe, making the 
                clocks run backward, or spinning the lab around on a carousel.
            </p>
            <p class="left-align">
                A good equation, Dr. Farmelo said, should be an economical 
                compression of truth without a symbol out of place. He looks for 
                attributes like universality, simplicity, inevitability, an 
                elemental power and "granitic logic" of the relationships 
                portrayed by those symbols. 
            </p>
            <p class="left-align">
                There is, for example, Einstein's E=mc², which Dr. Peter Galison, 
                a Harvard historian and physicist, describes in the book as "a 
                metonymic of technical knowledge writ large," adding, "Our 
                ambitions for science, our dreams of understanding and our 
                nightmares of destruction find themselves packed into a few 
                scribbles of the pen." 
            </p>
            <p class="left-align">
                When it comes to the quest for beauty in physics, even Einstein 
                was a piker compared with the British theorist Paul Dirac, who 
                once said "it is more important to have beauty in one's equations 
                than to have them fit experiment."
            </p>
            <p class="left-align">
                An essay by Dr. Frank Wilczek, a physics professor at the 
                Massachusetts Institute of Technology, recounts how the 
                25-year-old Dirac published an equation in 1928 purporting to 
                describe the behavior of the electron, the most basic and 
                lightest known elementary particle at the time. Dirac had arrived 
                at his formula by "playing around" in search of "pretty 
                mathematics," as he once put it. Dirac's equation successfully 
                combined the precepts of Einstein's relativity with those of 
                quantum mechanics, the radical rules that prevail on very small 
                scales, and it has been a cornerstone of physics ever since.
            </p>
            <p class="left-align">
                But there was a problem. The equation had two solutions, one 
                representing the electron, another representing its opposite, a 
                particle with negative energy and positive charge, that had never 
                been seen or suspected before.
            </p>
            <p class="left-align">
                Dirac eventually concluded that the electron (and it would turn 
                out every other elementary particle) had a twin, an antiparticle. 
                In Dirac's original interpretation, if the electron was a hill, a 
                blob, in space, its antiparticle, the positron, was a hole — 
                together they added to zero, and they could be created or 
                destroyed in matching pairs. Such acts of creation and 
                annihilation are now the main business of particle accelerators 
                and high-energy physics. His equation had given the world its 
                first glimpse of antimatter, which makes up, at least in 
                principle, half the universe.
            </p>
            <p class="left-align">
                The first antimatter particle to be observed, the antiproton, was 
                found in 1932, and Dirac won the Nobel Prize the next year. His 
                feat is always dragged forth as Exhibit A in the argument to 
                show that mathematics really does seem to have something to do 
                with reality.
            </p>
            <p class="left-align">
                "In modern physics, and perhaps in the whole of intellectual 
                history, no episode better illustrates the profoundly creative 
                nature of mathematical reasoning than the history of the Dirac 
                equation," Dr. Wilczek wrote.
            </p>
            <p class="left-align">
                In hindsight, Dr. Wilczek writes, what Dirac was trying to do was 
                mathematically impossible. But, like the bumblebee who doesn't 
                know he can't fly, through a series of inconsistent assumptions, 
                Dirac tapped into a secret of the universe.
            </p>
            <p class="left-align">
                Dirac had started out thinking of electrons and their opposites, 
                the "holes," as fundamental entities to be explained, but the 
                fact that they could be created and destroyed meant that they 
                were really evanescent particles that could be switched on and 
                off like a flashlight, explains Dr. Wilczek.
            </p>
            <p class="left-align">
                What remains as the true subject of Dirac's equation and as the 
                main reality of particle physics, he says, are fields, in this 
                case the electron field, which permeate space. Electrons and 
                their opposites are only fleeting manifestations of this field, 
                like snowflakes in a storm.
            </p>
            <p class="left-align">
                As it happens, however, this quantum field theory, as it is known, 
                must jump through the same mathematical hoops as Dirac's electron, 
                and so his equation survives, one of the cathedrals of science. 
                "When an equation is as successful as Dirac's, it is never simply 
                a mistake," Dr. Steven Weinberg, a 1979 Nobel laureate in physics 
                from the University of Texas, writes in an afterword to Dr. 
                Farmelo's book.
            </p>
            <p class="left-align">
                Indeed, as Dr. Weinberg has pointed out in an earlier book, the 
                mistake is often in not placing enough faith in our equations. In 
                the late 1940's, a group of theorists at George Washington 
                University led by Dr. George Gamow calculated that the birth of 
                the universe in a Big Bang would have left space full of fiery 
                radiation, but they failed to take the result seriously enough to 
                mount a search for the radiation. Another group later discovered 
                it accidentally in 1965 and won a Nobel Prize.
            </p>
            <p class="left-align">
                Analyzing this lapse his 1977 book, "The First Three Minutes," 
                Dr. Weinberg wrote: "This is often the way it is in physics. 
                Our mistake is not that we take our theories too seriously, but 
                that we do not take them seriously enough. It is always hard to 
                realize that these numbers and equations we play with at our desks 
                have something to do with the real world."
            </p>
